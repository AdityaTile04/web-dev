// let keyword;
// Syntax of declaring variables

let age = 19;
age = age + 1;

let cgpa;
cgpa = 8.5;

let num1 = 10;
let num2 = 20;
let sum = num1 + num2;

// const keyword:
// Values of constant can't be changed with re-assignment & they can be redeclared.

const year = 2024;
year = 2025; //error
year = year + 1; //error

const pi = 3.14;
const g = 9.8;

// var keyword:
// Old syntax of writing variables
// this is like let keyword

var myAge = 19;
myAge = myAge + 1;

var num_1 = 20;
var num_2 = 20;
var finalSum = num_1 + num_2;
